<script>
    $(document).ready(function() {

        loadCart();

        function loadCart() {

            $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });
            $.ajax({
                type: "GET",
                url: "{{url('/cart-count')}}",
                success: function(response) {
                    $('.cart-count').html('');
                    $('.cart-count').html(response.count);
                    //console.log(response.count);
                }
            });
        }

    });
</script>

